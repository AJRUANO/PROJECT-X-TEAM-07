    public void toProject() {
        table.setNumRows(weapons.size());
        for (int i = 0; i < weapons.size(); i++) {
            table.setValueAt(weapons.get(i).getName(), i, 0);
            table.setValueAt(weapons.get(i).getColor(), i, 1);
            table.setValueAt(weapons.get(i).getModel(), i, 2);
            table.setValueAt(weapons.get(i).getSerie(), i, 3);
            table.setValueAt(weapons.get(i).getDeliveryOfDay(), i, 4);
            table.setValueAt(weapons.get(i).getDayOfExit(), i, 5);

        }
        tblWeapon.setModel(table);
    }

