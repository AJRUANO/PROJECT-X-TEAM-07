    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!txtName.getText().equals("") && !txtColor.getText().equals("") && !txtModel.getText().equals("")&& !txtSerie.getText().equals("")&& !txtDeliDay.getText().equals("")&& !txtExDay.getText().equals("")) {
            Weapon weapon = new Weapon();
            weapon.setName(txtName.getText());
            weapon.setColor(txtColor.getText());
            weapon.setModel(txtModel.getText());
            weapon.setSerie(txtSerie.getText());
            weapon.setDeliveryOfDay(txtDeliDay.getText());
            weapon.setDayOfExit(txtExDay.getText());
            
            weapons.add(weapon);
            txtName.setText("");
            txtColor.setText("");
            txtModel.setText("");
            txtSerie.setText("");
            txtDeliDay.setText("");
            txtExDay.setText("");
            toProject();

        } else {
            JOptionPane.showMessageDialog(null, "");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
    if(tblWeapon.getSelectedRow()<0){
        JOptionPane.showMessageDialog(null, "Select record to delete");
    }else{
       weapons.remove(tblWeapon.getSelectedRow());
       toProject();
    }
    }//GEN-LAST:event_btnRemoveActionPerformed

